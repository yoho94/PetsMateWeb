package com.petsmate.dto;

public class DriverVO {
	String id, password, name, phone, car_number, car_model, car_color, car_image1, car_image2, driver_image, token;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getPhone() {
		return phone;
	}

	public void setPhone(String phone) {
		this.phone = phone;
	}

	public String getCar_number() {
		return car_number;
	}

	public void setCar_number(String car_number) {
		this.car_number = car_number;
	}

	public String getCar_model() {
		return car_model;
	}

	public void setCar_model(String car_model) {
		this.car_model = car_model;
	}

	public String getCar_color() {
		return car_color;
	}

	public void setCar_color(String car_color) {
		this.car_color = car_color;
	}

	public String getCar_image1() {
		return car_image1;
	}

	public void setCar_image1(String car_image1) {
		this.car_image1 = car_image1;
	}

	public String getCar_image2() {
		return car_image2;
	}

	public void setCar_image2(String car_image2) {
		this.car_image2 = car_image2;
	}

	public String getDriver_image() {
		return driver_image;
	}

	public void setDriver_image(String driver_image) {
		this.driver_image = driver_image;
	}

	public String getToken() {
		return token;
	}

	public void setToken(String token) {
		this.token = token;
	}
	
	
}
